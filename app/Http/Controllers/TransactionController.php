    public function detail($id)
    {
        $transaction = Transaction::with(['detail' => function ($query) {
            $query->with('items');
        }])->firstWhere('id', $id);
        if ($transaction) {
            return response()->json([
                'status' => 200,
                'message' => 'data found',
                'transaction' => $transaction,
                'formatted_date' => date_format($transaction->created_at, "d-m-Y H:i:s")
            ]);
        } else {
            return response()->json([
                'status' => 404,
                'message' => 'Data not found'
            ]);
        }
        return $transaction;
    }
